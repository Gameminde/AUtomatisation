def normalize_locale(locale: str | None) -> str:
    candidate = str(locale or "EN").strip().upper()
    return candidate if candidate in {"EN", "FR", "AR"} else "EN"


def get_system_dir(locale: str | None) -> str:
    return "rtl" if normalize_locale(locale) == "AR" else "ltr"


def translate(text: Any, locale: str | None = None, **params: Any) -> str:
    source = str(text or "")
    language = normalize_locale(locale)
    translated = TRANSLATIONS.get(language, {}).get(source, source)
    if params:
        try:
            return translated.format(**params)
        except Exception:
            return translated
    return translated


def get_catalog() -> Dict[str, Dict[str, str]]:
    return TRANSLATIONS
